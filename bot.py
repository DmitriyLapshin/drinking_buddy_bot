@dp.message(CommandStart())
async def cmd_start(message: types.Message):
    kb = InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text="🍻 Выпить", callback_data="drink")],
        [InlineKeyboardButton(text="🔮 Режим: Обычный", callback_data="toggle_riddle"),
         InlineKeyboardButton(text="📊 Статистика", callback_data="stats_btn")]
    ])
    await message.answer("Сегодня отличный день, чтобы выпить 🌅\nМонетка решит, пора ли начинать.", reply_markup=kb)

@dp.callback_query(F.data == "stats_btn")
async def cb_stats(callback: types.CallbackQuery):
    u = await get_user(callback.from_user.id)
    await callback.answer(
        f"🍷 Всего: {u['total_toasts']}\n"
        f"🛑 Остановлено: {u['stopped_count']}\n"
        f"🥂 Продолжено: {u['continued_count']}", show_alert=True
    )

@dp.callback_query(F.data == "toggle_riddle")
async def cb_toggle_riddle(callback: types.CallbackQuery):
    sess = get_session(callback.from_user.id)
    sess["riddle_mode"] = not sess["riddle_mode"]
    label = "🔮 Режим: Загадки" if sess["riddle_mode"] else "🔮 Режим: Обычный"
    await callback.answer(f"Режим изменён: {label}", show_alert=True)
    await cmd_start(callback.message)

